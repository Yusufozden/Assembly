#############################
# f = g - h
#############################

.data
f:		.word 10
g:		.word 20
h:		.word 30

.text
	.globl main
main:
	#-----------------------------------------------------------------
	lw $s2, f
	lw $s3, g
	lw $s4, h
	#-----------------------------------------------------------------
        sub $s2, $s3, $s4 # f = g - h
EXIT:
	#-----------------------------------------------------------------
	move $a0, $s2
	li $v0, 1           # syscall number 1 will print int in $a0
	syscall
	li $v0, 10          # Syscall number 10 is to terminate the program
	syscall             # exit now
	#-----------------------------------------------------------------
